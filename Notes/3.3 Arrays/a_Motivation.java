import java.util.Scanner;

public class a_Motivation {
  public static void main(String[] args) {
    /*
    As we continue to build more complex programs using loops,
    we might run into the following problem:
    Imagine a program where we need to keep track of every student's age. 

    We might write something like this:
    int age_1;
    int age_2;
    int age_3;
    int age_4;
    int age_5;
    ...

    This isn't great! 
    It also motivates the idea of a "data structure". 

    We might think of a data structure as a way
    to organize multiple pieces of data in one "thing".
    That "thing" is a data structure. 
    */
  }
}
