public class a_Motivation
{
    public static void main(String[] args)
    {
        /*
        We've spent a few units focusing on things that are quite complicated.
        Think about Wordle, or enhanced for loops, or 2-dimensional arrays.

        In this unit, we will make a return to something very fundamental to Java:
        the Object.

        Review Q: What "is" an Object?

        Answer: An Object is made of two things:
        1) Data (internal variables that store information)
        2) Methods (that do something to the data stored in an Object)

        As an example, String stores a sequence of characters (data),
        and has methods like .length() and .substring() (methods).

        Objects also tend to represent concrete ideas in our programs.
        Think about Cards, or Players, or ArrayLists. These are things
        that are concrete and we might imagine actually need to be represented. 

        Review Q: Why have we used Objects?

        Answer: We've needed to use Objects when we've needed to bundle together
        data and methods to represent something concrete in a program.

        For example, we've needed to use Student, or Card, or other Objects
        when we had data we needed to store AND methods to operate on that data.

        We could have just made a bunch of variables and methods in one file,
        but the fact is that by putting them all together in an Object
        we made our code less complex and easier to manage.
        */

        /*
        The above two questions might answer why we need Objects,
        but an astute observer might realize that all of the Objects 
        we've used in this class have been written by someone else.
        
        Since we can use them in our Java programs, they're coded in Java.
        However, someone else wrote that code. We just know how to use it
        from reading the instructions or using the API. 

        Today we'll figure out how to code our own Objects! 

        */

        /*
        The blueprint for an Object is called a Class.

        Each separate .java file is a Class. 

        Once we write the Class, we can create as many Objects
        from it as we want. 
        */
    }
}